int	ft_iterative_factorial(int nb)
{
	int	r;

	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	r = nb;
	while (nb > 1)
	{
		r *= (nb - 1);
		nb--;
	}
	return (r);
}
